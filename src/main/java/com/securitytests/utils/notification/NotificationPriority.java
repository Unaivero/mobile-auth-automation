package com.securitytests.utils.notification;

/**
 * Enum representing notification priority levels
 */
public enum NotificationPriority {
    /**
     * High priority notifications - urgent issues requiring immediate attention
     */
    HIGH,
    
    /**
     * Medium priority notifications - important issues that should be addressed soon
     */
    MEDIUM,
    
    /**
     * Low priority notifications - informational or minor issues
     */
    LOW
}
